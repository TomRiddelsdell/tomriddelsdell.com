// Get a specific translation
export function getTranslation(langCode: string, key: string): string {
  const language = languages.find(lang => lang.code === langCode);
  
  if (!language) {
    // Fallback to English
    const fallbackLang = languages.find(lang => lang.code === 'en');
    return fallbackLang?.translations[key] || key;
  }
  
  return language.translations[key] || key;
}

// Get language name from code
export function getLanguageName(langCode: string): string {
  const language = languages.find(lang => lang.code === langCode);
  return language?.name || langCode;
}
